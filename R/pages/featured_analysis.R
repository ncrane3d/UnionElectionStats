paperPanel <- function(paperName, authors, abstract) {
    accordion_panel(
        title = paperName,
        div(
            align = "left",
            div(strong("Authors: "), p(paste(authors, collapse = ", "))),
            div(strong("Abstract: "), p(abstract))
        ) %>%
            tagAppendAttributes(id = "accordion-analysis")
    )
}
